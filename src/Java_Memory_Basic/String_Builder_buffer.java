package Java_Memory_Basic;

public class String_Builder_buffer {

	public static void main(String[] args) {
		
		
		StringBuffer s1=new StringBuffer("Hello World");
		
		System.out.println(s1);
		
		String s2=new String("Shams");
		
		StringBuffer rev=s1.reverse();
		
		System.out.println(rev);
		
		
		
StringBuilder s3=new StringBuilder("Hello World");
		
		System.out.println(s1);
		
		String s4=new String("Rohny");
		
		StringBuilder rev2=s3.reverse();
		
		System.out.println(rev2);

	}

}
